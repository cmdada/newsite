            <h2>Your iPhone is Killing the Planet</h2>
            <div class="post-meta">
                <em>February 12, 2024</em> | by Ada
            </div>

            <div class="post-content">
                <p>For longer than I've understood electronics, for longer than I've been alive, the worst kept secret of the electronics industry is electronic waste; the grungy, sharp-edged side to the world's future. Be that through much of its seemingly easily preventable nature or the toxic and noxious, rancid smelling chemicals and their byproducts I nearly gag at the smell of, electronic waste leaves a taste almost as sour in my mouth as it leaves on the environment that it irreparably harms through its accumulation. The screaming, grinding cries of processing and recycling machines as I walk through a tour at eco-cycle proves the effort being put in towards mitigation of the problem. However, there is no easy solution to this issue in sight, even if everything around me now is promising. While technology marches forwards, despite the new convenience and ease it brings, in its dust are the rotting corpses of machines given up on too early by an economy that deprioritizes reuse and repair, made impossible to keep out of a landfill legally, and all this for no better reason than the bottom lines of faceless companies.</p>

                <p>In the economy we live under, for the past 99 years since the early days of the lightbulb, corporations have understood that consumers will purchase their product again when it breaks, incentivizing them to decrease product lifespans, and by proxy the number of their products in landfills. Planned obsolescence is a lasting and systemic issue of our economic system. Planned obsolescence is an institution in industry that has been going on for longer than anyone reading this will be alive, as illustrated in the paper Designed to Break: Planned Obsolescence as Corporate Environmental Crime, "Planned obsolescence entails the deliberate design of products to artificially limit their lifespan—either actual or perceived functionality—encouraging or requiring consumers to replace products prematurely (Sherif & Rice, 1986, p. 75). Light bulb producers originally used this strategy to increase their product turn-over and thus profitability, while New York real estate magnate Bernard London (1932, p. 2) went a step further by proposing a national policy of planned obsolescence to restore the US economy following the 1929 Wall Street crash. Robust products became economic liabilities in a Depression economy" (Bisschop, Hendlin, and Jaspers). While the Phoebus Cartel, the name the coalition of lightbulb manufacturers took, is an obvious, egregious example of this planned obsolescence for nearly a century, our economy incentivizes modern tech companies to use the same strategies on devices like iPhones, MacBook, printers, projectors and more to skyrocket their place in the economy and their stock price. In an interview I conducted with the local hard to recycle materials center (CHaRM), in response to asking "What sustainability goals do you have for the future of E-Waste recycling and processing?", logistics coordinator Dempsey Perno said "One goal is to work toward an economy that prioritizes repair and reuse as core values of how things function, instead of manufacturing obsolescence and refusing to allow consumers to repair their own materials. Electronic manufacturer should also become responsible for the waste they put into the world." This hard to swallow response proves the massive dilemma we are in, but proposes a solution; shifting responsibility from non-profits, volunteer organizations, and taxpayer funded operations to those who directly profit from ignoring the problem, the manufacturers. Bills to put this solution into action have rarely made it past local proposals, and of those that have, electronic manufacturers have lobbied non-stop over them. Despite this lobbying, companies like Nintendo, Microsoft, Apple, and Google put on a much more environmentally friendly look to consumers, in direct opposition to their lobbying.</p>

                <p>While it may seem simple to just start doing repairs, our political system makes repair hard, if not explicitly illegal. As the Electronic Freedom Frontier (a nonprofit organization dedicated to defending digital privacy, free speech, and innovation) puts it, "the software [on a broken device] may come with digital locks (aka Digital Rights Management [DRM] or Technical Protection Measures [TPMs]) supposedly designed to prevent unauthorized copying. And breaking those locks, even to do something simple and otherwise legal like tinkering with or fixing your own devices, means breaking the law, thanks to Section 1201 of the Digital Millennium Copyright Act" (EFF). The Digital Millennium Copyright Act that they talk about is notorious for many unconstitutional and pro-corporation acts, means that in repairing a friend's out of warranty MacBook with a battery that wouldn't charge recently, I broke the law to replace a serial protected battery that otherwise would have made the laptop trash to the friend, who needed a functional battery for long hours of school work and writing away from home. This practice of locking out parts by serial number is frequently used by apple to dissuade and attempt to stop repair, yet instead of this explicitly anti-consumer and waste generating law, the illegal part is circumvention of this "technical protection measure". Similarly, as outlined in the Tulane journal article "The Right to Repair and the Corporate Stranglehold over the Consumer: Profits over People", Kyle Montello illustrates the far-reaching effect of the DMCA and similar laws perfectly in saying "The DMCA, along with DRM [Digital Rights Management, a method for manufacturers to keep control over electronic devices after they are sold], has made its way from music and movies to smartphones, tractors, home appliances, and medical equipment" (Montello). This massive extent shown is exemplary of the wide-reaching paths of products that if not for this law, could be repaired, but due to the DMCA, at the simplest, smallest break, the device becomes a worthless piece of E-Waste that the company has no responsibility for and the consumer has to fully replace, instead of just the small part that broke; the $60 battery in the 1, 2, or even 3 thousand dollar MacBook.</p>
                <p>While a reasonable reader may assume that corporations have a good reason to block the right to repair, time and time again companies have given no good reason against right to repair legislation and policy. A greet example of this is when Apple Inc., in working on vetoing SB 4104 (the digital fair repair act, a proposed New York law heralded by repair and environmental experts as a small step in the right direction of keeping E-waste out of a landfill) has to engage in bald-faced lies to justify anti-repair practices, going as far as to say that "Self service repair of a battery is impossible for anyone but highly-trained technicians certified by Apple" (Foulkes 13), a lie that is instantly and easily disprovable by not only the market for batteries sold to individuals, but by just attempting a battery repair. In research for this, I replaced the battery in my 2015 iPhone 6s with relative ease in less than 10 minutes of work with a small set of tools from iFixit, a friend's iPhone 13 battery with a bit more trouble, but still relative ease in about 2 hours using the same tools, and a family member's iPhone 8 battery and display in under an hour and 20 minutes of work with the same set of tools as the first two and a strip of packing tape for removing the remnants of the screen glass.</p>


                <p>While E-Waste may seem to some as a problem only solvable with massive legislative orders and sweeping societal changes, there are many ways for an individual to make a difference. To many, electronic devices like phones, computers, and household appliances seem too complex to possibly fix, but this couldn't be further from the truth. Even if it isn't reasonable for you to fix a device, instead of sending it straight to the garbage and buying a newer model, consider bringing it to a local repair shop not only to attempt to keep your E-Waste down, but also to save a substantial amount of money. On the legal side, Colorado is a pioneer among states in terms of pro right to repair legislation, but the scope as of writing this among the two right to repair laws passed only includes wheelchairs and agricultural machinery. In the oncoming 2024 season, house bill HB-1121 proposes to amend Colorado Revised Statutes 6-1-1502(1)(b), (5)(a)(I), (5)(b), (5)(c) to include right to repair for consumer electronics. This amendment needs support, and any letters that can be written to a local governor or senator would help further the movement. Tell your legislator that you want the right to fix your electronics and keep them out of the landfill and serving their purpose.</p>

                <p>E-Waste and its impacts on Boulder County and the wider world are a direct consequence of the economic and political systems that prioritize planned obsolescence and restrict consumer rights to repair their own devices. This cycle of disposable phones, computers, appliances, and more electronics, fueled by corporate interests and legislative barriers, not only harms the environment but also undermines consumer rights and economic sustainability.</p>
